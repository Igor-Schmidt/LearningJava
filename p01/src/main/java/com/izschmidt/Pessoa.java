package com.izschmidt;

public class Pessoa {
    private String nome;
    private int idade;

    public Pessoa(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public int getIdade() {
        return this.idade;
    }

    public String getNome() {
        return nome;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void mostrarInfosPessoa(Pessoa pessoa) {
        System.out.printf("Nome: %s, Idade: %d %n", pessoa.nome, pessoa.idade);
    }
}
